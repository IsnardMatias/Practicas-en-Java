package practicas;

public class EjemploIfElseIfElse {
    
    public static void main(String[] args){
    
        int x = 5;
        if (x == 5) {
            System.out.println("X igual a 5\n");
        } else if (x == 10) {
            System.out.println("X igual a 10\n");
        } else if (x == 20) {
            System.out.println("X igual a 20\n");
        } else {
            System.out.println("X no es igual ni a 5, ni 10 ni 20\n");
        }
    }
}
